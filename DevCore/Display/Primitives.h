// *****************************************************************************
// ***   Box Class   ***********************************************************
// *****************************************************************************
class Box : public VisObject
{
  public:
    // *************************************************************************
    // ***   Constructor   *****************************************************
    // *************************************************************************
    Box() {};

    // *************************************************************************
    // ***   Constructor   *****************************************************
    // *************************************************************************
    Box(int32_t x, int32_t y, int32_t w, int32_t h, color_t c, bool is_fill = true);

    // *************************************************************************
    // ***   SetParams   *******************************************************
    // *************************************************************************
    void SetParams(int32_t x, int32_t y, int32_t w, int32_t h, color_t c, bool is_fill = true);

    // *************************************************************************
    // ***   SetBorderWidth   **************************************************
    // *************************************************************************
    void SetBorderWidth(int32_t width);

    // *************************************************************************
    // ***   GetBorderWidth   **************************************************
    // *************************************************************************
    int32_t GetBorderWidth() {return border_width;}

    // *************************************************************************
    // ***   SetColor   ********************************************************
    // *************************************************************************
    void SetColor(color_t c);

    // *************************************************************************
    // ***   GetColor   ********************************************************
    // *************************************************************************
    color_t GetColor() {return color;}

    // *************************************************************************
    // ***   SetBackgroundColor   **********************************************
    // *************************************************************************
    void SetBackgroundColor(color_t bgc);

    // *************************************************************************
    // ***   Put line in buffer   **********************************************
    // *************************************************************************
    virtual void DrawInBufH(color_t* buf, int32_t n, int32_t row, int32_t y = 0);

    // *************************************************************************
    // ***   Put line in buffer   **********************************************
    // *************************************************************************
    virtual void DrawInBufW(color_t* buf, int32_t n, int32_t line, int32_t x = 0);

  private:
    // Box color
    color_t color = COLOR_BLACK;
    // Box color
    color_t bg_color = COLOR_BLACK;
    // Is box fill ?
    bool fill = false;
    // Border width
    int32_t border_width = 0;
};

