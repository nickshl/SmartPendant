#ifndef NVM_h
#define NVM_h

// *****************************************************************************
// ***   Includes   ************************************************************
// *****************************************************************************
#include "DevCfg.h"

#include "Eeprom24.h"

// *****************************************************************************
// ***   Local const variables   ***********************************************
// *****************************************************************************

// *****************************************************************************
// ***   Defines   *************************************************************
// *****************************************************************************

// *****************************************************************************
// ***   NVM Class   ***************************************************
// *****************************************************************************
class NVM
{
  public:
    // *************************************************************************
    // ***   Get Instance   ****************************************************
    // *************************************************************************
    static NVM& GetInstance();

    // *************************************************************************
    // ***   Init function   ***************************************************
    // *************************************************************************
    Result Init(Eeprom24& in_eep);

    // *************************************************************************
    // ***   ReadData function   ***********************************************
    // *************************************************************************
    Result ReadData(void);

    // *************************************************************************
    // ***   GetCtrlTx function   **********************************************
    // *************************************************************************
    uint8_t GetCtrlTx() {return data.tx_ctrl;}

    // *************************************************************************
    // ***   SetCtrlTx function   **********************************************
    // *************************************************************************
    Result SetCtrlTx(uint8_t tx_ctrl);

    // *************************************************************************
    // ***   GetDisplayInvert function   ***************************************
    // *************************************************************************
    bool GetDisplayInvert() {return data.invert_display;}

    // *************************************************************************
    // ***   SetDisplayInvert function   ***************************************
    // *************************************************************************
    Result SetDisplayInvert(bool invert_display);

    // *************************************************************************
    // ***   SetUnits function   ***********************************************
    // *************************************************************************
    Result SetUnits(bool units);

  private:

    // Pointer to EEPROM object
    Eeprom24* eep = nullptr;

    // Struct with all settings we want to store in EEPROM
    typedef struct
    {
      uint8_t tx_ctrl = 0u;
      uint8_t invert_display = false;
    } Nvm_t;
    // Data
    Nvm_t data;

    // *************************************************************************
    // ***   Private constructor   *********************************************
    // *************************************************************************
    NVM() {};
};

#endif
